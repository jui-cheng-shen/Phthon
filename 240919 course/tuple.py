"""
這個模組展示了如何使用 Python 的元組 (tuple)。
功能包括：
- 定義元組
- 訪問元組中的元素
- 元組的不可變性
- 連接元組
- 在循環中使用元組
範例：
    定義元組:
    訪問元組中的元素:
    連接元組:
    在循環中使用元組:
"""
a = ("apple", "banana", "cherry")  # 定義一個包含三個元素的元組
b = ("apple",)  # 定義一個包含一個元素的元組，注意逗號是必要的


print(a)  # 輸出元組 a
print(b)  # 輸出元組 b

print(type(a))  # 輸出 a 的類型，應該是 <class 'tuple'>
print(type(b))  # 輸出 b 的類型，應該是 <class 'tuple'>